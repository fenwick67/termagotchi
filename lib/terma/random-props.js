var sexes = ['male','female'];// Make a PR if you want intersex termagotchis... I am already overwhelmed enough by this project


// get a random set of colors for terma
function randomColors(){
  return{
    primary:randomFrom(colorPairs),
    secondary:randomFrom(colorPairs)
  };
}

function randomNameAndSex(){
  var sex = randomFrom(sexes);
  var list = (sex=='male')?maleNames:femaleNames;
  var name = randomFrom(list);
  return {
    name:name,
    sex:sex
  }  
}

module.exports = {
  nameAndSex:randomNameAndSex,
  colors:randomColors
}
  
// helpers  
  
// pick a random element from an array
function randomFrom(list){
  if (!list.length || list.length < 0){return undefined;}
  return list[ Math.round( Math.random()* (list.length - 1) )];
}